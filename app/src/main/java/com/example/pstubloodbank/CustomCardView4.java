public class CustomCardView4 {
    private String name;
    private String bloodgroup;
    private String email;

    public CustomCardView4(String name,String bloodgroup, String email) {
        this.name = name;
        this.bloodgroup = bloodgroup;
        this.email = email;
    }


    public String getName() {
        return name;
    }

    public String getBloodgroup() {
        return bloodgroup;
    }

    public String getEmail() {
        return email;
    }


}

